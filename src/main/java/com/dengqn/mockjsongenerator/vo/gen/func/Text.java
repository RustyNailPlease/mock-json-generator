    public static Text fromPattern(String pattern) {
        String replace = pattern.replace("{{", "").replace("}}", "");
        String[] split = replace.split("\\|");
        if (split.length == 1) {
            return new Text(1, 3);
        }
        String[] range = split[1].split(",");
        if (range.length == 1) {
            return new Text(1, Integer.parseInt(range[0]));
        }
        return new Text(Integer.parseInt(range[0]), Integer.parseInt(range[1]));
    }

